//simple for loop. where three parts need to control the loop
//variable declaration;loop condition;variable changes
for(let num=0;num<5;num++){
    console.log(num);
}

//Printing even number using for loop smaller than 40
for(let num=0;num<40;num++){
    if(num%2!==1){
        console.log(num)
    }
}

//Printing odd number less than 40 in desending order
for(let num=40;num>0;num--){
    if(num%2!==0){
        console.log(num)
    }
}
